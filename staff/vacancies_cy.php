<h3>Athro Parhaol Celf</h3>

<p>Rydym yn chwilio am athro/athrawes ysbrydoledig ac uchelgeisiol i ymuno ag adran lwyddiannus iawn yn yr ysgol. Bydd disgwyl i’r ymgeisydd llwyddiannus i addysgu Celf ar draws bob cyfnod allweddol, gan gyfrannau at gyrsiau poblogaidd TGAU a Safon Uwch. Yn ogystal, bydd angen cyfrannu at addysgu CA3 yn yr adran Dylunio a Thechnolega bydd y gallu i gynnig Ffotograffiaeth at Safon Uwch yn fanteisiol iawn.</p> 

