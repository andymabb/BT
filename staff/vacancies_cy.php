<h3>Hysbyseb Athro / Arweinydd Ffiseg</h3>
<p>Mae corff Llywodraethu yr ysgol am benodi Arweinydd yn yr adran Ffiseg. Rydym yn chwilio am berson sydd â hanes o ardderchogrwydd o ran addysgu, yn gallu ysbrydoli disgyblion a chydweithwyr ac yn barod i ddatblygu strategaethau blaengar i symud yr adran lwyddiannus yma yn ei blaen eto.</p>
<p>Ers agor yn 2003, mae Bryn Tawe wedi ennill bri fel ysgol gartrefol, lwyddiannus ac uchelgeisiol ar gyfer eu holl ddisgyblion wedi ei leoli ar gyrion Dinas Abertawe, yn gyfleus iawn ar gyfer yr M4. Mae staff gweithgar a thalentog yr ysgol yn allweddol wrth yrru llwyddiannau’r ysgol yn ogystal â sicrhau’r ymdeimlad cryf o gymuned sydd yn bodoli yma.</p>
<p>Rydym yn chwilio am athro/athrawes graddedig gyda’r brwdfrydedd, profiad, egni a’r dychymyg i barhau i ddatblygu’r gwaith da a gyflawnwyd eisoes yn yr adran Ffiseg. Yn benodol, rydym yn chwilio am unigolyn sydd â’r weledigaeth a’r sgiliau i allu arwain a datblygu Ffiseg ymhellach yng NghA5 yn yr ysgol. Yn ogystal â’r cyfrifoldeb ychwanegol yma, ceir cyfle i addysgu dosbarthiadau ar draws yr ystod oed a gallu ac mae pwyslais cryf ar gynnig gwaith ymarferol i’n disgyblion sydd yn dod a’r pwnc yn fyw!</p>
<p>Mae gan yr adran Wyddoniaeth saith labordy gyda’r cyfarpar diweddaraf. Mae’r Gwyddorau’n ddewisiadau poblogaidd ar gyfer Safon Uwch ac rydym yn cynnig Gwyddoniaeth Driphlyg ar lefel TGAU. Cymraeg yw iaith addysgu’r adran Wyddoniaeth ac fe fydd disgwyl i’r ymgeisydd llwyddiannus addysgu’r pwnc ar draws pob cyfnod allweddol a chynnal ac ymestyn ystod o weithgareddau allgyrsiol o fewn cyfadran gynhaliol a blaengar.</p>
<p>Rydym yn gallu cynnig:</p>
<ul>
	<li>y cyfle i weithio gyda disgyblion brwdfrydig, hapus a gweithgar</li>
	<li>y cyfle i weithio o fewn adran Wyddoniaeth sydd yn ymrwymedig i gydweithio’n agos er mwyn sicrhau llwyddiant pob disgybl o bob gallu.</li>
	<li>y cyfle i weithio gyda tîm o athrawon a staff talentog, proffesiynol a blaengar, sydd yn llwyr ymrwymedig i sicrhau llwyddiant yr ysgol ac yn barhaus yn barod i fynd yr ail filltir er lles ein disgyblion.</li>
	<li>y cyfle i ddysgu’n broffesiynol mewn ysgol gartrefol ble mae cefnogaeth i’r holl staff yn allweddol.</li>
	<li>y potensial ar gyfer datblygiad gyrfa pellach o fewn a thu allan i’r adran a’r ysgol.</li>
</ul>
<p>Os ydych chi’n berson sydd yn meddu ar y rhinweddau rydym ni yn chwilio amdanynt, byddem yn falch iawn i glywed gennych. Mae croeso i chi gysylltu â rheolwr busnes yr ysgol, Mrs Eirian Leonard am fanylion pellach.</p>
