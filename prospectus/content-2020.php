<h2 id="transfer">Transfer of pupils from the Partner Primary Schools</h2>
<p>A great deal of preparation is made beforehand to ensure a smooth a transition from our Primary partner schools. We are very aware that transition from Primary school can mean quite a dramatic change for many pupils, therefore, we are very keen to get to know our partner Primary school pupils well before they arrive at Bryn Tawe, so that they settle in as quickly as possible.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p2-l50.jpg" class="l50" alt="">
<p>As part of our successful transition programme our primary school pupils will visit Bryn Tawe throughout Year 6 on varied activities related to learning and socialising with colleagues and pupils from all the primary schools. These involve animation, science and food technology lessons, an entrepreneurial competition, sport skills sessions and team building activities, &ldquo;taster&rdquo; lessons in a range of subjects and the transition residential course to Llangrannog. You will receive a timetable of all the activities and dates as part of our transition programme.</p>
