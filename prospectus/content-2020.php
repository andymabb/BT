<p>The school refectory will provide a cafeteria system where pupils may choose their meals from a set daily menu. We will ensure that there is ample opportunity for pupils to select from a range of healthy and appealing foods.</p>
<h3>Health screening and vaccinations</h3>
<p>During their life at Bryn Tawe pupils will be subject to health screening and vaccinations. You will be informed by letter before such an event and your permission sought.</p>
<h3>Mobile Phones</h3>
<p>The school recognises the usefulness of mobile phones, particularly in times of emergency and also that they can contribute towards educational progress e.g. by using an educational &lsquo;App&rsquo; on a Smartphone. Pupils may use mobile phones in school <strong>under certain defined conditions with permission from a member of staff</strong>. Please see the mobile phone policy for full details. Pupils who are in breach of the policy will have their phones safely retained by staff until parents/guardians are available to collect them. We appreciate the support of parents/guardians in these matters.</p>

