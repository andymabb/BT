<p id="intro">Thank you for the interest you are taking in Ysgol Gyfun Gymraeg Bryn Tawe. It is a pleasure to present our prospectus to you as potential parents and pupils of the school.</p>
<p>We share the privilege as parents, pupils and staff of contributing to a school that has a strong ethos of aiming for excellence in every aspect of our pupils&rsquo; lives. Our pupils have achieved consistently strong outcomes in their external examination results over time, which allows them to follow a career path of their choice. This is clear evidence of the strong partnership that exists between the school and the community, and we extend a warm invitation to you to become a part of this successful partnership. In this prospectus you will read of the very high expectations we have of ourselves, our parents and our students.</p>
<p>Included in the prospectus is a list of our <em>Aims and Objectives</em> as a school. These form the cornerstone of what we do at Bryn Tawe on a daily basis and we ensure that pupils are at the heart of everything that we do. We provide a safe, stimulating and caring environment where each individual pupil is given the support and encouragement to grow and develop both socially and intellectually.</p>
<p>We are very proud of the learning facilities at the school. Our talented and hard working staff provide high quality opportunities and experiences within lessons, as well as through a wide range of extra-curricular activities, which enriches the lives of our pupils. We have a committed governing body with several members having taken a lead role in establishing the school.</p>
<p>All we expect of our students is a commitment to working hard, a determination to gain as much as they can from their educational experiences at Bryn Tawe, a desire to speak Welsh both in lessons and socially and to conduct themselves in such a way as to give themselves and their peers the best possible opportunity to succeed at school. These expectations are best summarised in our three core principles &ndash; Respect, Welsh Ethos and Resilience.</p>
<p>This handbook is the first step in forging a genuine partnership between your home and the school. Together, we can ensure that your sons and daughters receive the best possible educational provision, which will provide them with a first class start in life. I look forward to developing a positive and successful relationship with you.</p>
<p><strong>Simon Davies</strong><br>Head</p>
<p class="c"><strong>Chair of Governors: Mr Heini Gruffudd</strong></p>
