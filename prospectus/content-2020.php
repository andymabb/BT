<p>We have clear and comprehensive policies to safeguard all that work and visit Bryn Tawe. The school follows the &ldquo;All Wales Child Protection Procedure 2008&rdquo;. For full details or a copy of the policy please contact the school. If you wish to discuss any safeguarding matter please contact the school&rsquo;s Designated Safeguarding Officer &ndash; Mr Mark Bridgens (Assistant Head) or our Deputy Designated Safeguarding Officer, Mrs Bethan Williams (ALN Co-ordinator). Equally, please feel free to contact the Governing Body designated representatives for Safeguarding &ndash; Mrs Bethan James and Mrs Adele Thomas. Their contact details are available from the school - please contact Mrs Eirian Leonard, the school Business Manager for details.</p>
