  <li>a consultation evening in October with the Personal Tutor looking at pastoral issues, settling in etc.</li>
  <li>an Interim Subject Report in December with feedback on progress</li>
  <li>a consultation evening with subject staff in looking at progress</li>
  <li>a full written report with detailed progress in each subject.</li>
</ul>
<p>However, as a school, we welcome informal enquiries at any time from parents who wish to discuss their child&rsquo;s educational progress. Should you wish to arrange to meet a member of staff, please telephone or email the school and we shall endeavour to respond as quickly as possible, usually on the same day. We ask parents not to call at the school without first making an appointment.</p>
