<h2 id="aims">Aims and Objectives</h2>
<h3>Our Mission</h3>
<p>&ldquo;To provide a comprehensive Welsh medium education within a happy, caring and ambitious community where each member is appreciated, is supported and is treated with respect and tolerance. We strive to achieve excellence in all aspects of school life so that every pupil can reach their full potential.&rdquo;</p>
<p>We aim to make this a reality by:</p>

<ul>
<li>ensuring excellent standards of teaching and learning which challenge and stimulate every learner</li>
<li>offering a wide and balanced curriculum which is appropriate for every pupil and which develops ambitious, creative, principled, healthy and confident learners and citizens</li>
<li><img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p1-r50.jpg" class="r50" alt="">ensuring that every pupil takes full advantage of their bi-lingual skills and to develop pride in and knowledge of the culture, history and traditions of Wales</li>
<li>ensuring that every pupil has opportunities to develop their literacy, numeracy, digital literacy, problem solving, collaborative and thinking skills, in order to become a competent member of the future workforce</li>
<li>providing and developing a pastoral structure that recognises the needs of every pupil and offers the necessary support, information and guidance</li>
<li>offering wide ranging extracurricular activities that include sport, performing, local and international visits, charity and community participation</li>
<li>providing an environment where moral and spiritual values are respected</li>
<li>maintaining high expectations of behaviour and school uniform</li>
<li>encouraging all pupils to live healthy lifestyles and to enjoy a variety of activities</li>
<li>developing a close and productive relationship with all members of the school community, with regular opportunities to share information</li>
<li>ensuring that our pupils&rsquo; voice and opinions are at the heart of all aspects of school life.</li>
</ul>
<p>Based on our aims and objectives, we have 3 core principles that are central to everything we do at Bryn Tawe on a daily basis. Our school councils have taken responsibility on behalf of all stakeholders to develop a clear meaning for these three core principles. </p>
