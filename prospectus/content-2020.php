<h2 id="support">Support for Student Learning</h2>
<p>If students are to be successful learners they will need support and guidance. The teaching staff will set the expectations of the student at all Key Stages and monitor progress towards them. Should students show signs of failing to reach these expectations, the school will provide a guidance programme, which is specific to the student.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p20-l50.jpg" class="l50" alt="">
<h3>Meeting the needs of all students</h3>
<p>We want the best for all our students and our pastoral and well being programme is designed to ensure that students lead happy, welladjusted lives in order to succeed by achieving their potential. All pupils&rsquo; work and progress are regularly and carefully evaluated and assessed informally during the term, and formally through unit tests.</p>
<p>Students who show signs of particular abilities or who wish to extend and develop their skills in specific areas will be offered extension and more challenging tasks and activities inside and outside of formal lessons.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p21-r50.jpg" class="r50" alt="">
<p>Those students who, on the other hand, find certain areas of the curriculum more challenging or who require support for their learning will be given additional resource in the form of focused support sessions and/or coaching from learning mentors as well as support within the learning environment.</p>
<h2 id="aln">Additional Learning Needs (ALN)</h2>
<p>As an inclusive, Rights Respecting School, we believe in providing:</p>
<ul>
  <li>a high quality education for all</li>
  <li>equal educational opportunities for all pupils</li>
  <li>an education that meets the individual needs of all pupils</li>
  <li>The Additional Learning Needs Department have responsibility for identification of pupils via a school-based assessment and audit. This will involve diagnostic assessment, support and monitoring functions.</li>
  <li>Following an assessment, individual pupils will receive specific support to develop their literacy and / or numeracy skills at the appropriate level required for their learning needs.</li>
  <li>In-class support will be focused on those activities, which need specific support and guidance.</li>
  <li>Individual pupils will receive bespoke support for their learning need outside of the mainstream lessons.</li>
  <li>We also provide specific support for pupils in groups to meet their learning needs.</li>
  <li>Pupils will receive specific interventions from the school and outside agencies that will support the more complicated needs of individual pupils e.g. the Educational Psychology Service and the Behaviour Support Unit.</li>
  <li>As well as support to improve learning, pupils also receive interventions to support the development of skills including personal organisation, accepting responsibility, resilience, creative thinking and analysis, team-working, goal setting, planning own learning, recording progress, assessing performance, reflecting on what has been learned and celebrating success.</li>
</ul>
