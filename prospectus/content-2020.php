<h3>Appointments</h3>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p25-r50.jpg" class="r50" alt="">
<p>We request that appointments for doctors and dentists etc. are made, where possible, outside of the school day. If this is unavoidable students must produce a letter signed by a parent / guardian in order to be released from lessons. The letter should be taken to the school office where the student will be given an authorised pass to leave the school premises.</p>
<h3>Family holidays in term time</h3>
<p>We are committed to raising levels of attendance in order to raise levels of achievement. We would ask that holiday arrangements are made in line with school holidays. It is already clear that the strategy of actively discouraging the taking of holidays in term time has resulted in a significant improvement in attendance at Bryn Tawe and across schools in the City and County of Swansea.</p>
<p>The School therefore intends to pursue this strategy as part of its drive to raise attendance. Generally, absences due to holidays during term times will not be authorised by the Headteacher. The Headteacher will consider each request on an individual basis before determining a response. This means that absences due to pupils being taken on holidays during term time without the authorisation of the Headteacher will be recorded as unauthorised.</p>
<h3>Illness</h3>
<p>If a pupil is taken ill whilst at school every effort is made to keep the child comfortable. If students need to take prescribed medication, this should be left in the school office. It is the pupil&rsquo;s responsibility to come to the office in order to take the medication at the appropriate times.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p26-r50.jpg" class="r50" alt="">
<p>Whenever necessary, we notify parents and the student is taken home. It is the duty of the parents to make arrangements for the pupil to be collected from school and taken home or to the doctor or hospital.</p>
<p>Parents are asked to contact us on the first day of absence and to provide the pupil with an absence note. Unless the school has been informed of the reason of absence, we will contact the home on the first day of absence by text message. In the event of a prolonged absence, the school will make arrangements for work to be sent home where appropriate.</p>

<h2 id="bullying">Bullying</h2>
<p>Bullying can take many forms, both overt and covert. Bullying is contradictory to the ethos of Bryn Tawe and will not be tolerated.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p27-r50.jpg" class="r50" alt="">
<p>Every student at Bryn Tawe has the right be an individual, and has the right to be educated in a happy, stress-free environment. Pupils who are bullied by whatever means are unlikely to be happy in school, and are likely to be less successful. Through our Anti-Bullying policy we promote respect and tolerance at every opportunity and continually remind our pupils that any bullying type behaviour is not tolerated at the school. We use school and year assemblies, PSE sessions and support the Anti-Bullying week etc. to promote this message strongly.</p>
<p>We will adopt whatever means available to combat bullying. These methods may include:</p>
<ul>
  <li>verbal warning</li>
  <li>restorative circles</li>
  <li>the use of Anti-Bullying contracts</li>
  <li>involvement of parents</li>
  <li>the direct use of sanctions</li>
  <li>interview by the school council</li>
</ul>
<p><em>Any incidents of bullying should be reported immediately by the pupil and/or parents.</em></p>

<h2 id="coc">Bryn Tawe&rsquo;s Code of Conduct</h2>
<p>We have high expectations for the behaviour of our pupils, as is seen in our three core principles under &lsquo;Respect&rsquo;. This means that we ensure negative behaviour does not damage the opportunity for all to learn and be happy at school. This applies to all who work at Bryn Tawe.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p28-r50.jpg" class="r50" alt="">
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p29-r33.jpg" class="r33" alt="">
<p>As a Restorative School, we have adopted Restorative Practices as a core element to promote positive behaviour within the school community. The aim of Restorative Practice is to develop the community and to manage conflict and tensions by repairing harm and building relationships.</p>
<p>We are very proud of the high standards in Bryn Tawe with regards to respect, courtesy, and behaviour. Through adopting Restorative Practices fully we can ensure:</p>
<ul>
  <li>that pupils are central to the discipline process</li>
  <li>that the positive ethos continues inside and outside of class</li>
  <li>that we continue to develop the positive relationships between pupils and staff as well as between pupils</li>
</ul>
<p>We ensure that all our pupils are clear about our expectations of their behaviour, as is explained in our three core principles. Disrespectful behaviour is not acceptable at Bryn Tawe and firm action will be taken in all cases. Parents will be informed and involved when appropriate and may be asked to come in to discuss any issues.</p>
<p><em>Bryn Tawe is a school where teachers are given the opportunity to teach, and pupils are given the opportunity to learn &ndash; without disruption.</em></p>
<h3>Behaviour consequences</h3>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p30-r50.jpg" class="r50" alt="">
<p>Should any pupil not meet the high expectations we set at Bryn Tawe, the school may use sanctions such as the ones below, depending on the seriousness of the offence:</p>
<ul>
  <li>subject area detention</li>
  <li>after school detention</li>
  <li>report card &ndash; this will be sent home to be signed</li>
  <li>community service</li>
  <li>a period of exclusion from a specific lesson / subject</li>
  <li>internal exclusion, students will work in silence in isolation for a set period in our fully staffed inclusion room, &lsquo;Y Gilfach&rsquo;</li>
  <li>fixed term or permanent exclusion, for serious offences.</li>
</ul>
<p>We promote a climate where staff or pupils at Bryn Tawe will not tolerate unacceptable behaviour.</p>
