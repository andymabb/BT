<h3>Health and Safety/School Security</h3>
<p>We will be vigilant in all matters concerning health and safety issues. Risk assessments are carried out both in school and for any off-site activities which have risks associated with them. Accidents involving personal injury are recorded and reported to the school&rsquo;s Health and Safety Officer. The school site is completely fenced with wide ranging CCTV inside and outside the building. Health and Safety audits and action plans are regularly carried out and updated. Health and safety issues are discussed on a regular basis in the meetings of the Governing Body and the Health and Safety sub-committee.</p>

<h3>Smoking</h3>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p33-r50.jpg" class="r50" alt="">
<p>Following the LA policy, we have adopted a no smoking policy for all pupils, staff and visitors. We know that smoking can seriously damage the health of smokers and that of people in the vicinity. Therefore, smoking, including e-cigarettes is not acceptable anywhere within the grounds of Bryn Tawe.</p>
<h3>Security of personal belongings</h3>
<p>The students are responsible for their personal belongings. We ask students not to bring items of significant value to school. Students will be provided with safe storage for musical instruments. We will endeavour to provide all students with personal lockers. A charge of &pound;3.00 is made for all students who would like a locker at the start of Year 7. The Physical Education staff will have their own security measures for pupils&rsquo; personal belongings.</p>
<h3>Fire Drill</h3>
<p>All pupils will know what to do in case of fire. Posters outlining the procedure will be posted in every teaching and learning room. In order that this procedure is reinforced a Fire Drill will be a regular feature at Bryn Tawe. The school is fortunate in having a sprinkler system.</p>
