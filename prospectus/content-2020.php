<h2 id="tl">Teaching and Learning</h2>
<p>Developing teaching is our main priority at the school, in order to support pupils to become:</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p10-r33.jpg" class="r33" alt="">
<ul>
  <li><strong>Ambitious, capable learners who are ready to learn throughout their lives.</strong></li>
  <li><strong>Enterprising, creative contributors who are ready to play a full part in life and work.</strong></li>
  <li><strong>Ethical, informed citizens who are ready to be citizens of Wales and the world.</strong></li>
  <li><strong>Healthy, confident individuals who are ready to lead fulfilling lives as valued members of society.</strong></li>
</ul>
<p>As a &lsquo;Curriculum Pioneer School&rsquo; and a &lsquo;Lead Creative School&rsquo;, there is a continual focus on evaluating a wide range of very effective learning and teaching strategies. We have very high expectations that enable every individual pupil to learn to their potential through:</p>
<ul>
  <li>detailed planning of learning in lessons,</li>
  <li>providing creative and interesting activities that inspire pupils to learn and deepen understanding,</li>
  <li>challenging all pupils of all abilities to learn to their potential,</li>
  <li>incisive questioning and strategies that support every pupil to build understanding,</li>
  <li>developing high quality learning practices that are established over time,</li>
  <li>supporting pupils to lead their own learning,</li>
  <li>making very effective use of assessment for learning strategies that support pupils to ensure deep learning and understanding.</li>
</ul>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p11-l50.jpg" class="l50" alt="">
<p>All learning is underpinned by a well-developed Pastoral Programme focused on the individual. Our purpose is to prepare our students effectively for the opportunities, responsibilities and experiences of life within a bilingual society.</p>
<h3>Key Stage 3</h3>
<p>Our curriculum in Key Stage 3 has been planned using our pupils&rsquo; previous learning in our partner Primary schools. There is a clear focus on developing the Literacy, Numeracy and Digital Competence of all our pupils. As well as ensuring opportunities to develop vital subject knowledge in lessons, pupils are provided with opportunities to learn in a creative manner, to develop independent learning skills and to collaborate and co-operate with peers.</p>
<h3>Year 7</h3>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p12-r50.jpg" class="r50" alt="">
<p>In Year 7, all pupils will undertake a broad-based curriculum that will be skills and competence based and will equip them to be effective learners and develop the skills for life and lifelong learning. Welsh, English, Maths, Science, Physical Education, Modern Foreign Languages and Design Technology are taught in discrete lessons. All Yr 7 pupils follow the CAMU course, which is a thematic based course with a strong emphasis on developing literacy, numeracy and digital competence within the context of History, Geography, Religious Education, Drama, Art and ICT. Personal and Social Education is delivered via whole school thematic mornings six times a year.</p>
<h3>Key Stage 4</h3>
<p>Compulsory core subjects are studied by every pupil &ndash; Welsh, English, Maths, Science, ICT, Physical Education, Religious Studies and Personal and Social Education. Three options are also chosen from a wide range of academic and vocational subjects within the context of Learning Pathways. We have developed several strong partnerships with local post 14 learning providers in order to offer a progressive learning pathway from KS4 to KS5 in our Sixth Form. Please see the KS4 options booklet for full information on our curriculum offer at KS4.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p13-l33.jpg" class="l33" alt="">
<h3>Key Stage 5</h3>
<p>We have established a very successful partnership with Ysgol Gyfun G&#x175;yr and created the Bryn Tawe &ndash; G&#x175;yr post 16 partnership that is the main post 16 Welsh medium learning provider within the City and County of Swansea. For full details of our post 16 provision, please ask for a copy of the separate Sixth Form prospectus.</p>

<h2>Personal and Social Education (PSE)</h2>
<p>Our PSE curriculum is delivered through six focus mornings during the year. These sessions focus on developing pupils&rsquo; knowledge on a range of relevant themes as well as the relevant skills that will allow each pupil to make informed and responsible life choices.</p>

<h2 id="sex">Sex Education</h2>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p14-r50.jpg" class="r50" alt="">
<p>Appropriate and sensible sex education is an important element in the work of the school in preparing students for adult life. Care and sensitivity will be ensured in the delivery of the lessons at the appropriate age for all our pupils. If you wish to withdraw your child from these classes, which may conflict with your religious, cultural or moral beliefs, please make a written application to the Head.</p>
<p><strong>The aims of the policy are:</strong></p>
<ul>
  <li>to present facts in an objective balanced manner</li>
  <li>to know what is and what is not legal</li>
  <li>to help pupils make informed, reasoned and responsible decisions</li>
  <li>to ensure a moral framework in which students are encouraged to consider the importance of self-restraint, dignity and respect for themselves and others</li>
  <li>to enable pupils to appreciate the benefits of stable family life, and the responsibilities of parenthood</li>
  <li>to be aware of the inherent dangers of casual sex</li>
</ul>

<h2 id="homework">Independent Study and Homework</h2>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p15-r33.jpg" class="r33" alt="">
<p>All students at Bryn Tawe are responsible for their learning. While the work of staff will support learning, ultimately only the student can ensure success. As part of ensuring success and developing discipline in learning, all students are expected to complete homework and independent study. Regular independent study supports students in working on their own, planning their own time and taking responsibility for their own progress. Success at Key Stages 3, 4 and 5 depends on students mastering these personal study skills.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p16-l33.jpg" class="l33" alt="">
<p>It is the policy at Bryn Tawe that homework is given on a regular basis with feedback provided by staff. Students are provided with a Student Planner that is checked by subject staff regularly. We request that parents look at these as often as possible and let us know whether a student is spending too much or too little time on homework.</p>
<p>The amount of homework set and time spent on it will vary, but in Year 7 it will normally take between one and one and half hours each evening.</p>
<p>The kind of homework set will also vary; it may include creative writing, problem solving exercises, research work, revision for tests, watching a particular TV programme, internet research, preparing an oral presentation etc.</p>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p17-r50.jpg" class="r50" alt="">
<h3>The Student Planner</h3>
<p>The planner will play a vital part in the day-to-day contact between home and school. It will provide teachers with a convenient channel of communication through which to praise students for good work and progress, and also to raise concerns with parents. We are currently trialling the &lsquo;Show my Homework&rsquo; app as a digital option to the student planner with years 10 and 11, with a view of extending this to the whole school in 2018-19.</p>
<h2 id="consulting">Consulting with Parents</h2>
