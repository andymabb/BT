<h3>The Importance of Parental Participation</h3>
<p>The relationship between Bryn Tawe and its parents is crucial, and this is particularly true in terms of the parents of children with additional learning needs.</p>
<p>We believe it is important that:</p>
<ul>
  <li>parents should be seen as equal partners in the process</li>
  <li>parents are aware of the school&rsquo;s ALN policy</li>
  <li>parents are aware of the support available for ALN children within the school and the LA</li>
  <li>parents are aware of their rights to be involved and of the importance of their contribution</li>
  <li>parents should be given any information on local and national voluntary organisations which might provide information, advice and/or counselling</li>
