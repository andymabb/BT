<h2 id="careers">Addysg Yrfaol</h2>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p41-r50.jpg" class="r50" alt="">
<p>Mae pob disgybl o flwyddyn 8 ymlaen yn mynychu gwersi penodol ar addysg yrfaol ac entrepreneuriaeth fel rhan o&rsquo;r rhaglen ABCh.</p>
<p>Mae gan bob aelod o flwyddyn 11 yr hawl i dderbyn cyfweliad gyda&rsquo;r Swyddog Gyrfaoedd sydd yn cynrychioli &lsquo;Gyrfa Cymru&rsquo;. Rydym yn targedu gr&#x175;p bach o ddisgyblion ym Mlwyddyn 11 i dderbyn arweiniad pellach gan swyddog gyrfaoedd Gyrfa Cymru. Mae&rsquo;r disgyblion yn derbyn cefnogaeth gyson ar wahanol lwybrau sydd ar gael iddynt &ocirc;l-16 oed a&rsquo;r sgiliau personol a chymdeithasol sydd angen arnynt. Mae gan yr ysgol record ardderchog o weld disgyblion o bob ystod gallu yn gwneud trosglwyddiad llwyddiannus i addysg, prentisiaeth, y byd gwaith neu hyfforddiant &ocirc;l-16 oed. Mae disgyblion yn elwa&rsquo;n fawr o ddiwrnodau ymgynghori yn ystod y cyfnodau trawsnewid cwricwlaidd ym mlynyddoedd 9 ac 11.</p>
<p>Ar gyfartaledd mae dros 95% o ddisgyblion ysgol Bryn Tawe yn parhau mewn addysg lawn amser ar ddiwedd CA4. Mae dros 80% o ddisgyblion sy&rsquo;n gadael ar &ocirc;l blwyddyn 13 yn symud ymlaen i addysg uwch.</p>
