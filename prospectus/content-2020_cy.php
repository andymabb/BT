<p>Disgwylir i bawb gymryd rhan yn yr ystod eang o weithgareddau a drefnir gan yr Adran Addysg Gorfforol. Mae&rsquo;r cwricwlwm yn cynnwys ffitrwydd ac iechyd, sgiliau sylfaenol, athletau, gymnasteg, dawns, hoci, p&ecirc;l-rwyd, rygbi, p&ecirc;l-droed, tennis, rownderi a thraws gwlad.</p>
<p>Y nod yw caniat&aacute;u i gynifer o ddisgyblion &acirc; phosib fwynhau&rsquo;r amrywiaeth ehangaf posib o weithgareddau chwaraeon a ffitrwydd.</p>
<p>Mae pwyslais hefyd ar gymryd rhan mewn chwaraeon cystadleuol. Hybir hyn trwy gystadlaethau ar lefel Llys, Blwyddyn, Sir, Cymru gyfan a Phrydain! Disgwylir i&rsquo;r holl ddisgyblion ddod &acirc;&rsquo;r cit cywir i bob gwers Addysg Gorfforol. Os oes cyflwr meddygol a allai atal cymryd rhan o dro i dro, dylid nodi hynny trwy lythyr rhieni / tystysgrif feddygol.</p>
<p>Mae&rsquo;r ysgol yn rhoi pwys mawr ar ddatblygiad y person cyfan, a meithrin arferion ac agweddau iach a fydd yn fuddiol i ffordd o fyw'r disgyblion wrth iddynt dyfu&rsquo;n oedolion.</p>
