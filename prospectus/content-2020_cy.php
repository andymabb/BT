<h2 id="aims">Nodau ac Amcanion</h2>
<h3>Ein cenhadaeth</h3>
<p>&ldquo;Darparu addysg gyfrwng Cymraeg, gyflawn o fewn cymuned hapus, ofalgar ac uchelgeisiol lle mae pob aelod yn cael eu gwerthfawrogi, yn cael cefnogaeth ac yn cael eu trin gyda pharch a goddefgarwch. Anelwn at ragoriaeth ym mhob agwedd o fywyd yr ysgol er mwyn i bob un o&rsquo;r disgyblion gyflawni eu potensial llawn.&rdquo;</p>
<p>Fe fyddwn yn gwireddu hyn drwy:</p>
<ul>
<li>sicrhau safonau dysgu ac addysgu ardderchog sydd yn herio ac ysbrydoli pob disgybl</li>
<li>gynnig cwricwlwm eang a phwrpasol sydd yn addas i bob disgybl ac sy&rsquo;n datblygu dysgwyr a dinasyddion uchelgeisiol, creadigol, egwyddorol, iachus a hyderus.</li>
<li><li><img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/inline/p1-r50.jpg" class="r50" alt="">sicrhau cyfleoedd i bob disgybl fanteisio&rsquo;n llawn ar eu sgiliau dwyieithog ac i feithrin balchder mewn ac ymwybyddiaeth o ddiwylliant, hanes a thraddodiadau Cymru</li>
<li>sicrhau cyfleoedd i bob disgybl ddatblygu sgiliau llythrennedd, rhifedd, cymhwysedd digidol, datrys problemau, cydweithio a meddwl er mwyn dod yn aelod llawn o weithlu&rsquo;r dyfodol</li>
<li>gynnal a datblygu strwythur bugeiliol sy&rsquo;n adnabod anghenion pob disgybl ac yn darparu&rsquo;r gefnogaeth, gwybodaeth a chymorth angenrheidiol</li>
<li>gynnig profiadau allgyrsiol niferus ym meysydd chwaraeon, perfformio, ymweliadau lleol a rhyngwladol, elusennau a gweithgareddau cymunedol</li>
<li>gynnal a datblygu awyrgylch lle mae parch at werthoedd moesol ac ysbrydol</li>
<li>gynnal disgwyliadau uchel o ran ymddygiad a gwisg ysgol</li>
<li>annog disgyblion i fyw bywydau iach ac i fwynhau gweithgareddau chwaraeon amrywiol</li>
<li>ddatblygu perthynas agos a chynhyrchiol gyda holl aelodau cymuned yr ysgol, gyda
chyfleoedd cyson i rannu gwybodaeth</li>
<li>sicrhau r&ocirc;l ganolog i farn disgyblion o fewn holl weithgarwch yr ysgol</li>
</ul>
<p>Yn deillio o&rsquo;n nodau ac amcanion, mae yna dair egwyddor sylfaenol gennym sydd yn greiddiol i bopeth a wnawn bob dydd. Ein cynghorau ysgol sydd wedi cymryd y cyfrifoldeb o ddatblygu ystyr y tair egwyddor ar gyfer rhan ddeiliaid yr ysgol gyfan.</p>
