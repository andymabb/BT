<a href="#toc" class="toc-link">Tabl Cynnwys</a>
<img src="https://www.bryntawe.swansea.sch.uk/images/prospectus-2018/logo.png" alt="Logo" class="logo">
<p id="intro">Diolch i chi am fynegi diddordeb yn Ysgol Gyfun Gymraeg Bryn Tawe. Mae&rsquo;n bleser cael cyflwyno&rsquo;n prosbectws i chi fel darpar rieni a disgyblion yr ysgol.</p>
<p>Rhannwn y fraint fel rhieni, disgyblion a staff yr ysgol o gyfrannu at ysgol sydd ag ethos cryf o anelu am ragoriaeth ym mhob agwedd o fywydau ein disgyblion. Mae canlyniadau ein disgyblion mewn arholiadau allanol dros amser yn gyson uchel, sydd yn eu caniat&aacute;u i ddilyn eu llwybr dewisol mewn bywyd. Mae hyn yn dystiolaeth o gryfder y bartneriaeth sydd yn bodoli rhwng yr ysgol a&rsquo;i chymuned, ac fe'ch gwahoddwn chi&rsquo;n gynnes i fod yn rhan o&rsquo;r bartneriaeth lwyddiannus yma. Yn y prosbectws byddwch yn darllen am y disgwyliadau uchel sydd gennym ar ein cyfer ni ein hunain, ein rhieni a&rsquo;n myfyrwyr.</p>
<p>Yn y prosbectws ceir rhestr o&rsquo;n <em>Nodau a&rsquo;n Hamcanion</em> fel ysgol. Rhain ydy cerrig sylfaen yr hyn a wnawn yn ddyddiol ym Mryn Tawe a sicrhawn ein bod yn gosod ein disgyblion yng nghanol popeth a wnawn. Rydym yn darparu amgylchedd diogel, ysgogol a gofalgar lle bydd pob disgybl yn cael y gefnogaeth a&rsquo;r anogaeth i dyfu a datblygu&rsquo;n gymdeithasol ac yn ddeallusol.</p>
<p>Mae cyfleusterau dysgu ardderchog yn yr ysgol. Mae ein staff gweithgar a thalentog yn sicrhau cyfleoedd a phrofiadau o&rsquo;r radd flaenaf yn y gwersi a thrwy raglen o weithgareddau allgyrsiol eang sy&rsquo;n cyfoethogi bywyd ein disgyblion. Mae gennym gorff llywodraethu ymroddedig gyda sawl aelod wedi bod yn flaenllaw yn y broses o sefydlu&rsquo;r ysgol.</p>
<p>Rydym yn disgwyl i&rsquo;n disgyblion ymrwymo i weithio&rsquo;n galed, i fanteisio i&rsquo;r eithaf ar y profiadau addysgol ym Mryn Tawe, i siarad Cymraeg yn y gwersi ac yn gymdeithasol ac ymddwyn mewn modd a fydd yn cynnig y cyfle gorau posib iddynt hwy a&rsquo;u cyfoedion lwyddo yn yr ysgol. Mae&rsquo;r disgwyliadau yma wedi eu crynhoi o fewn ein tair egwyddor &ndash; Parch, Cymreictod a Dyfalbarhad.</p>
<p>Y llawlyfr hwn yw&rsquo;r cam cyntaf yn llunio partneriaeth rhwng y cartref a&rsquo;r ysgol. Gyda&rsquo;n gilydd, gallwn sicrhau y bydd eich meibion a&rsquo;ch merched yn cael y ddarpariaeth addysgol orau bosib a fydd yn rhoi&rsquo;r dechreuad gorau mewn bywyd iddynt. Edrychaf ymlaen at sefydlu partneriaeth gref rhyngom, er lles eich plentyn.</p>
<p><strong>Simon Davies</strong><br>Pennaeth</p>
<p class="c"><strong>Cadeirydd y Llywodraethwyr: Mr Heini Gruffudd</strong></p>
